void	*ft_memccpy(char *s1, char *s2, char c, size_t n)
{
	unsigned long	i;

	i = 0;
	while (i < n)
	{
		s1[i] = s2[i];
		if (s2[i] == c)
			return (&(s1[i + 1]));
		i++;
	}
	return (0);
}
