char	*ft_strchr(char *str, int chr)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (chr == '\0')
		{
			if (str[i + 1] == '\0')
				return (&str[i + 1]);
		}
		if (str[i] == chr)
			return (&str[i]);
		i++;
	}
	return (0);
}
